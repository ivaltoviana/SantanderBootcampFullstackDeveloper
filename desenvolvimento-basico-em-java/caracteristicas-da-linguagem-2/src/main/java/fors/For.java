package fors;

public class For {

    public static void main(String[] args) {

        // para i iniciando em 0, enquanto i for menor ou igual a 10 incrementa mais 1 ao i
        for (int i = 0; i <= 10; i = i + 1) {
            System.out.println("I = " + i);
        }

        System.out.println("\n");

        for (int x = 0; x <= 5; x++) {
            System.out.println("X = " + x);
        }
    }

}
