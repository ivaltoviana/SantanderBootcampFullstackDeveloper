package operadores;

public class Matematicos {

    public static void main(String[] args) {
        // adição
        System.out.println(0 + 1);

        // subtração
        System.out.println(3 - 1);

        // multiplicação
        System.out.println(8 * 7);

        // divisão
        System.out.println(5 / 5);

        // módulo - resto da divisão
        System.out.println(5 % 5);

        var numero = 10;
        // numero = numero * 2;
        numero *= 2;
        System.out.println(numero);
    }
}
